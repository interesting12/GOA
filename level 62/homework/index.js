// 1. Multiply Two Numbers
function multiply() {
    const num1 = 5;
    const num2 = 10;
    console.log( num1 * num2);
}

// 2. Subtract Two Numbers
function subtract() {
    const num1 = 6;
    const num2 = 2;
    console.log(num1 - num2);
}

// 3. Divide Two Numbers
function divide() {
    const num1 = 10;
    const num2 = 2;
    if (num2 !== 0) {
        console.log (num1 / num2);
    } else {
        console.log ("Cannot divide by zero");
    }
}

